# ---------------------------------------------------------------------------
# Telegram ID → approximate registration year ranges
# These are rough estimates based on publicly known Telegram ID thresholds.
# ---------------------------------------------------------------------------
_TG_ID_YEAR_RANGES = [
    (10_000_000, 2013),
    (100_000_000, 2014),
    (300_000_000, 2015),
    (500_000_000, 2016),
    (750_000_000, 2017),
    (1_000_000_000, 2018),
    (1_500_000_000, 2019),
    (2_000_000_000, 2020),
    (3_000_000_000, 2021),
    (4_000_000_000, 2022),
    (5_000_000_000, 2023),
    (6_000_000_000, 2024),
]

# 2FA field name candidates (case-insensitive)
_2FA_FIELD_NAMES = {"twofa", "passwordfa", "2fa", "two_fa", "password"}


def get_country_from_phone(phone: str) -> Tuple[str, str, str]:
    """Return (country_name, country_flag, country_code) for the given phone number.

    The *phone* string may start with '+' or be digits-only.
    Returns ('未知', '', '') when no match is found.
    """
    digits = phone.lstrip("+").strip()
    if not digits:
        return ("未知", "", "")

    # Try longest prefix first (4 → 3 → 2 → 1 digits)
    for length in (4, 3, 2, 1):
        prefix = digits[:length]
        if prefix in PHONE_PREFIX_MAP:
            return PHONE_PREFIX_MAP[prefix]

    return ("未知", "", "")


def estimate_registration_date(telegram_id: Optional[int]) -> Optional[str]:
    """Return a human-readable registration year estimate based on Telegram user ID.

    Returns a string like '2019年' or '2019-2020年', or None when the ID is
    unavailable or below the earliest known threshold.
    """
    if not telegram_id:
        return None

    uid = int(telegram_id)
    prev_year = None
    for threshold, year in _TG_ID_YEAR_RANGES:
        if uid < threshold:
            if prev_year and prev_year != year:
                return f"{prev_year}-{year}年"
            return f"{year}年"
        prev_year = year

    # ID is beyond all known thresholds
    current_year = datetime.now(timezone.utc).year
    return f"{current_year}年"


def parse_2fa_from_json(config: dict) -> Optional[str]:
    """Extract a 2FA password/secret from an account JSON config dict.

    Looks for keys: twofa, passwordfa, 2fa, two_fa, password (case-insensitive).
    Returns the value as a string, or None if not found / empty.
    """
    for key, value in config.items():
        if key.lower() in _2FA_FIELD_NAMES and value:
            return str(value)
    return None
